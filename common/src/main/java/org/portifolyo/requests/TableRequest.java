package org.portifolyo.requests;

public class TableRequest {

   private int page;
   private int size;

   public TableRequest(){}
    public TableRequest(int page, int size) {
        this.page = page;
        this.size = size;
    }

    public int getPage() {
        return page;
    }

    public void setPage(int page) {
        this.page = page;
    }

    public int getSize() {
        return size;
    }

    public void setSize(int size) {
        this.size = size;
    }
}
